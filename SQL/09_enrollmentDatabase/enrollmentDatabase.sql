 CREATE DATABASE ENROLLMENT;
USE ENROLLMENT;

CREATE TABLE STUDENT(
    SNUM INT(10),
    SNAME VARCHAR2(20),
    MAJOR VARCHAR2(20),
    LVL VARCHAR2(3),
    AGE INT(2),
    PRIMARY KEY(SNUM));

CREATE TABLE FACULTY(
    FID INT(10),
    FNAME VARCHAR2(20),
    DEPTID INT(1),
    PRIMARY KEY(FID)); 

CREATE TABLE CLASS(
    CNAME VARCHAR2(5),
    MEETS_AT TIMESTAMP,
    ROOM INT(3),
    FID INT(10),
    PRIMARY KEY(CNAME),
    FOREIGN KEY(FID) REFERENCES FACULTY(FID));

CREATE TABLE ENROLLED(
    SNUM INT(10),
    CNAME VARCHAR2(5),
    PRIMARY KEY(SNUM),
    FOREIGN KEY(SNUM) REFERENCES STUDENT(SNUM),
    FOREIGN KEY(CNAME) REFERENCES CLASS(CNAME));

